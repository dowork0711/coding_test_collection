# 별찍기 8

n = int(input())

for i in range(1, n+1):   # 1 ~ 2n
  print("*"*i + " "*(n-i) + " "*(n-i) + "*"*i)

for i in range(n-1, 0, -1): # n-1 ~ 1
  print("*"*i + " "*(n-i) + " "*(n-i) + "*"*i)

# *        *        1 : 2
# **      **        2 : 4
# ***    ***        3 : 6
# ****  ****        4 : 8
# **********        5 : 10        n: 2n
