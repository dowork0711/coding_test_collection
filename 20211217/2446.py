# 별찍기 - 9

n = int(input())

for i in range(n):
  print(" "*i, end='')    # end: 맨끝에 처리, print 따로 쓰면 띄어쓰기 되니까 띄어쓰기를 없애줌
  print("*"*(2*(n-i)-1))

for i in range(n-2, -1, -1):
  print(" "*i, end='')
  print("*"*(2*(n-i)-1))

# *********   5 : 9   2n-1
#  *******    4 : 7
#   *****     3 : 5
#    ***      2 : 3
#     *       1 : 1
# ------------
#    ***      1 : 3
#   *****     2 : 5
#  *******    3 : 7
# *********   4 : 9   2n+1